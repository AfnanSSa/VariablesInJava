//this is Float values Calculator which appplies the main mathematical operations on float values.


class HelloWorld {
    public static void main(String[] args) {
        float firstNum = 7.5f;
        float secondNum = 14.7f;
		
		//Addition
        System.out.println("Addition: " + (firstNum + secondNum));
		
		//Subtraction
        System.out.println("Subtraction: " + (secondNum - firstNum));
		
		//Multiplication
        System.out.println("Multiplication: " + (firstNum * secondNum));
		
		//Division
        System.out.println("Division: " + (firstNum/secondNum));
    }
}