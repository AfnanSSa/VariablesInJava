//this is Long values Calculator which appplies the main mathematical operations on long values.


class HelloWorld {
    public static void main(String[] args) {
        long firstNum = 7L;
        long secondNum = 14L;
		
		//Addition
        System.out.println("Addition: " + (firstNum + secondNum));
		
		//Subtraction
        System.out.println("Subtraction: " + (secondNum - firstNum));
		
		//Multiplication
        System.out.println("Multiplication: " + (firstNum * secondNum));
		
		//Division
        System.out.println("Division: " + (firstNum/secondNum));
    }
}