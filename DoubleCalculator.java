//this is Double Calculater which appplies the main mathematical operations on double value.


class HelloWorld {
    public static void main(String[] args) {
        double firstNum = 10.5;
        double secondNum = 20.5;
		
		//Addition
        System.out.println("Addition: " + (firstNum + secondNum));
		
		//Subtraction
        System.out.println("Subtraction: " + (secondNum - firstNum));
		
		//Multiplication
        System.out.println("Multiplication: " + (firstNum * secondNum));
		
		//Division
        System.out.println("Division: " + (firstNum/secondNum));
    }
}