//this is Short values Calculator which appplies the main mathematical operations on short value.


class HelloWorld {
    public static void main(String[] args) {
        short firstNum = 7;
        short secondNum = 14;
		
		//Addition
        System.out.println("Addition: " + (firstNum + secondNum));
		
		//Subtraction
        System.out.println("Subtraction: " + (secondNum - firstNum));
		
		//Multiplication
        System.out.println("Multiplication: " + (firstNum * secondNum));
		
		//Division
        System.out.println("Division: " + (firstNum/secondNum));
    }
}