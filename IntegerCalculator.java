//this is Integer Calculater which appplies the main mathematical operations on integers.


class HelloWorld {
    public static void main(String[] args) {
        int firstNum = 10;
        int secondNum = 20;
		
		//Addition
        System.out.println("Addition: " + (firstNum + secondNum));
		
		//Subtraction
        System.out.println("Subtraction: " + (secondNum - firstNum));
		
		//Multiplication
        System.out.println("Multiplication: " + (firstNum * secondNum));
		
		//Division
        System.out.println("Division: " + (firstNum/secondNum));
    }
}